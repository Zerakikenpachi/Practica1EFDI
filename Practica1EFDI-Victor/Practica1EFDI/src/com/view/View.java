    private void GuardarColeccionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarColeccionActionPerformed
        if (p.CrearColeccion(txtTrimestre.getText(), txtId.getText())) {
            txtTrimestre.setText("");
            txtId.setText("");
            JOptionPane.showMessageDialog(null, "Los datos se guardaron");
        } else {
            txtTrimestre.setText("");
            txtId.setText("");
            JOptionPane.showMessageDialog(null, "Verifique, ha ocurrido un error");
        }
    }//GEN-LAST:event_GuardarColeccionActionPerformed

    private void ListarColeccionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ListarColeccionActionPerformed
        DefaultTableModel model = (DefaultTableModel) TablaColeccion.getModel();
        for (int i = 0; i < TablaColeccion.getRowCount(); i++) {
            model.removeRow(i);
            i -= 1;
        }

        List<Coleccion> coleccion = p.ConsultarColecciones();
        for (Coleccion q : coleccion) {
            model.addRow(new Object[]{q.getId(), q.getTrimestre()});
        }
    }//GEN-LAST:event_ListarColeccionActionPerformed

