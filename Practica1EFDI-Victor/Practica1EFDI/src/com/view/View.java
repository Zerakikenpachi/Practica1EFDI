    private void ListarTipProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ListarTipProductoActionPerformed
        DefaultTableModel model = (DefaultTableModel) TablaTipProducto.getModel();

        for (int i = 0; i < TablaTipProducto.getRowCount(); i++) {
            model.removeRow(i);
            i -= 1;
        }

        List<TipoProducto> tipproducto = q.ConsultarTipoProductos();
        for (TipoProducto q : tipproducto) {
            model.addRow(new Object[]{q.getReferencia(), q.getNombre()});
        }
    }//GEN-LAST:event_ListarTipProductoActionPerformed

    private void ColeccionProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ColeccionProductoActionPerformed
        String data;
        data = JOptionPane.showInputDialog("Ingrese la coleccion con la que desea guardar el producto");
        data.trim();
        if (p.ColeccionEsta(data) != null) {
            txtColeccion.setText(p.ColeccionEsta(data));
        } else {
            JOptionPane.showMessageDialog(null, "El producto no está");
        }
    }//GEN-LAST:event_ColeccionProductoActionPerformed

